struct Loan {
    id: u32,
    amount: u32,
    share: u32,
}
impl Loan {
    fn new(id: u32, amount: u32, share: u32)->Self{
        Self{
            id,amount,share
        }
    }fn inc_amount(&mut self,pro:u32)->u32{
        self.amount+=self.share*pro;
        self.amount
    }
}
fn main(){
    let mut l=Loan::new(3,30000,3);
    let y=l.inc_amount(3);
    println!("value {}",y)
}