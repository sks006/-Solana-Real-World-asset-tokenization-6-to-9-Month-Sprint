enum Status{
    pass,
    reje
}
struct Loan{
    acc:u32,
    status:Status
}

impl Loan{
    fn new(a:u32,b:Status)->Self{
        Self{
            acc:a,
            status:b
        }
    }
}

fn main(){
    let l=Loan::new(4323,Status::pass);
    match l.status{
        Status::pass=>println!("pass {}",l.acc),
        Status::reje=>println!("reje {}",l.acc),  
    }
}